# Define the askYesOrNo function
askYesOrNo() {
    local question="$1"
    
    # Prompt the user for a single character "y" or "n"
    read -n 1 -p "$question (y/n): " answer
    echo
    
    # Check if the answer is "y" (case-insensitive)
    if [[ "$answer" =~ ^[Yy]$ ]]; then
        return 0
    else
        return 1
    fi
}

